/**
 * Transform Figma tokens into tokens.ts format
 */
function generateTokensFile(figmaTokens: FigmaToken[]): string {
  const tokensByType: Record<string, Record<string, string>> = {}

  // Group tokens by type
  figmaTokens.forEach((token) => {
    if (!tokensByType[token.type]) {
      tokensByType[token.type] = {}
    }
    tokensByType[token.type][token.name] = token.value
  })

  // Generate TypeScript code
  const tokensCode = `/**
 * Design System Tokens (AUTO-GENERATED from Figma)
 * DO NOT EDIT - Generated by MCP sync
 * Last updated: ${new Date().toISOString()}
 */

export const tokens = {
${Object.entries(tokensByType)
  .map(([type, tokens]) => {
    const entries = Object.entries(tokens)
      .map(([key, value]) => `    ${key}: '${value}',`)
      .join('\n')
    return `  ${type}s: {\n${entries}\n  },`
  })
  .join('\n')}
} as const

export type TokenKey = keyof typeof tokens
${Object.keys(tokensByType)
  .map((type) => `export type ${type.charAt(0).toUpperCase() + type.slice(1)}Key = keyof typeof tokens.${type}s`)
  .join('\n')}
`

  return tokensCode
}

/**
 * Transform Figma components into contracts.ts format
 */
function generateContractsFile(figmaComponents: FigmaComponent[]): string {
  const componentsObj: Record<string, any> = {}

  figmaComponents.forEach((component) => {
    const propsObj: Record<string, any> = {}

    component.props.forEach((prop) => {
      propsObj[prop.name] = {
        type: prop.type,
        required: prop.required,
        ...(prop.values && { values: prop.values }),
        ...(prop.default !== undefined && { default: prop.default }),
      }
    })

    componentsObj[component.name] = { props: propsObj }
  })

  const contractCode = `/**
 * Design System Contract (AUTO-GENERATED from Figma)
 * DO NOT EDIT - Generated by MCP sync
 * Last updated: ${new Date().toISOString()}
 */

export const DS_CONTRACT = {
  components: ${JSON.stringify(componentsObj, null, 4).replace(/"(\w+)":/g, '$1:').replace(/"(enum|string|boolean|function|ReactNode)"/g, "'$1' as const")},
  tokens: {
    // Token keys available - imported from tokens.ts
    colors: [${figmaComponents.flatMap((c) => c.props.filter((p) => p.type === 'enum' && p.name.includes('color')).flatMap((p) => p.values || [])).map((v) => `'${v}'`).join(', ')}],
    // Add other token categories as needed
  },
  rules: [
    'Only use components listed in the contract',
    'Only use props defined for each component',
    'Use only token keys for styling (no hardcoded values)',
    'Required props must always be provided',
    'Enum props must use one of the specified values',
  ],
} as const

export type DSContract = typeof DS_CONTRACT
export type ComponentName = keyof typeof DS_CONTRACT.components
`

  return contractCode
}

/**
 * Main sync function - fetches from Figma and regenerates files
 */
export async function syncFromFigma(options: {
  outputDir?: string
  dryRun?: boolean
}): Promise<{ success: boolean; filesGenerated: string[]; errors: string[] }> {
  const { outputDir = './src/design-system', dryRun = false } = options
  const filesGenerated: string[] = []
  const errors: string[] = []

  try {
    console.log('🔄 Fetching design system from Figma via MCP...')
    const figmaData = await fetchFigmaDesignSystem()

    console.log(`✓ Found ${figmaData.tokens.length} tokens`)
    console.log(`✓ Found ${figmaData.components.length} components`)

    // Generate tokens.ts
    const tokensCode = generateTokensFile(figmaData.tokens)
    const tokensPath = join(outputDir, 'tokens.ts')
    
    if (!dryRun) {
      writeFileSync(tokensPath, tokensCode, 'utf-8')
      console.log(`✓ Generated ${tokensPath}`)
      filesGenerated.push(tokensPath)
    } else {
      console.log(`[DRY RUN] Would write: ${tokensPath}`)
    }

    // Generate contracts.ts
    const contractsCode = generateContractsFile(figmaData.components)
    const contractsPath = join(outputDir, 'contracts.ts')
    
    if (!dryRun) {
      writeFileSync(contractsPath, contractsCode, 'utf-8')
      console.log(`✓ Generated ${contractsPath}`)
      filesGenerated.push(contractsPath)
    } else {
      console.log(`[DRY RUN] Would write: ${contractsPath}`)
    }

    console.log('✅ Sync complete!')
    
    return { success: true, filesGenerated, errors }
  } catch (error) {
    const errorMsg = error instanceof Error ? error.message : String(error)
    errors.push(errorMsg)
    console.error('❌ Sync failed:', errorMsg)
    return { success: false, filesGenerated, errors }
  }
}

/**
 * CLI entry point
 * Usage: tsx src/ai/mcp-sync.ts [--dry-run]
 */
if (import.meta.url === `file://${process.argv[1]}`) {
  const dryRun = process.argv.includes('--dry-run')
  
  syncFromFigma({ dryRun })
    .then(({ success, filesGenerated }) => {
      if (success) {
        console.log('\n📦 Generated files:')
        filesGenerated.forEach((file) => console.log(`  - ${file}`))
        process.exit(0)
      } else {
        process.exit(1)
      }
    })
    .catch((err) => {
      console.error('Fatal error:', err)
      process.exit(1)
    })
}

